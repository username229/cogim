// Sistema de tradução
class AdminTranslationSystem {
    constructor() {
        this.currentLanguage = localStorage.getItem('cogim_admin_lang') || 'pt';
        this.translations = adminTranslations;
    }
    
    setLanguage(lang) {
        this.currentLanguage = lang;
        localStorage.setItem('cogim_admin_lang', lang);
        this.updatePageTranslations();
        this.updateLanguageButtons();
    }
    
    translate(key) {
        return this.translations[this.currentLanguage][key] || key;
    }
    
    updatePageTranslations() {
        document.querySelectorAll('[data-translate]').forEach(element => {
            const key = element.getAttribute('data-translate');
            const translation = this.translate(key);
            
            if (element.tagName === 'INPUT' && (element.type === 'submit' || element.type === 'button')) {
                element.value = translation;
            } else if (element.tagName === 'OPTION') {
                element.textContent = translation;
            } else {
                element.textContent = translation;
            }
        });
    }
    
    updateLanguageButtons() {
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.classList.remove('active');
            if (btn.getAttribute('data-lang') === this.currentLanguage) {
                btn.classList.add('active');
            }
        });
    }
    
    init() {
        // Event listeners para botões de idioma
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.addEventListener('click', () => {
                const lang = btn.getAttribute('data-lang');
                this.setLanguage(lang);
            });
        });
        
        // Aplicar traduções iniciais
        this.updatePageTranslations();
        this.updateLanguageButtons();
    }
}

